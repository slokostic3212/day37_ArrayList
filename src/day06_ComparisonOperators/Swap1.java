package day06_ComparisonOperators;

public class Swap1 {

    public static void main(String[] args) {

        int a = 10;
        int b = 15;

        int c = b; // c = 15;

        b = a; // = 10
        a = c; // a = 10 now can assign c

        System.out.println(a); // 15
        System.out.println(b); // 10




    }


}
