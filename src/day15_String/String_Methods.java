package day15_String;

public class String_Methods {
    public static void main(String[] args) {
        String name = "Cybertek";
        //             01234567
        // charAt(index):
            char ch1 = name.charAt(0);// this returns char C
            char ch2 = name.charAt(5);

        System.out.println(ch1);// prints out c
        System.out.println(ch2);// prints out t





    }
}
